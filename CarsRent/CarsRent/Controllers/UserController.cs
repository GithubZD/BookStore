        //
        // POST: /Manage/ChangePassword
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult ChangePassword(ChangePasswordViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }
            var user = db.Users.SingleOrDefault(u => u.LoginName == User.Identity.Name);
            string oldPassword = SecurityPsw.SHA1PAssword(model.OldPassword);
            if (user.Password != oldPassword)
            {
                ModelState.AddModelError("OldPassword", "原密码错误");
            }
            else
            {
                string newPassword = SecurityPsw.SHA1PAssword(model.NewPassword);
                user.Password = newPassword;
                db.SaveChanges();
                MessageBox.Show("修改密码成功，请重新登陆！");
                AuthenticationManager.SignOut(DefaultAuthenticationTypes.ApplicationCookie);
                return RedirectToAction("Login", "Account");
            }
            return View();
        }

        public ActionResult AddIcon(HttpPostedFileBase iconFile,int? UserID)
        {
            if (iconFile == null|| UserID==null)
            {
                var result = new
                {
                    status = 0
                };
            return Json(result);
            }
            else
            {
                var user = db.Users.Find(UserID); 
                string imageName = Guid.NewGuid().ToString() + iconFile.FileName;
                string pathName = Server.MapPath("~/Images/IconImg/" + imageName);
                iconFile.SaveAs(pathName);
                user.Icon = "/Images/IconImg/" + imageName;
                db.SaveChanges();
                var result = new
                {
                    status = 1
                };
                return Json(result);
            }
        }

